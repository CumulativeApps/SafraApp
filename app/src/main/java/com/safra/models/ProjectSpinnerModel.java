package com.safra.models;


public class ProjectSpinnerModel {
    private int id;
    private String name;

    // Getters and setters

    @Override
    public String toString() {
        return name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
